### Cansting tipe data


data = 2.98

# float
data_float = float(data)
print('Data:', data_float,'\ttype:',type(data_float))

# intejer
data_int = int(data)
print('Data:', data_int,'\ttype:',type(data_int)) ### Bilangan Pecahan akan di bilatkan ke bawah oleh intejer

# boolean
data_bool = bool(data)
print('Data:', data_bool,'\ttype:',type(data_bool)) ### Semua biangan bernilai True baik + atau -     (Kecuali 0)
### boolean akan False jika nilai str kosong

# string
data_str = str(data)
print('Data:', data_str,'\ttype:',type(data_str))